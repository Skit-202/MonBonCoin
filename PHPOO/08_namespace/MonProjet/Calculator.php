<?php

namespace Math;
class Calculator
{
    public static function add(int $a, int $b)
    {
        return $a + $b;
    }

    public static function remove(int $a, int $b)
    {
        return $a - $b;
    }

    public static function multiply(int $a, int $b)
    {
        return $a * $b;
    }

    public static function divising(int $a, int $b)
    {
        return $a / $b;
    }
    
}

